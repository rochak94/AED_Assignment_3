package universitymodel.Department;

public class University {

	String universityName ;
	int universityRanking;
	int numberOfCareerFair;
	int numberOfProf;
	int numberOfDepartment;
	int numberOfCoursesOffered;
	int fundsforRndProjects;	

	
	public String getUniversityName() {
		return universityName;
	}
	public int getFundsforRndProjects() {
		return fundsforRndProjects;
	}
	public void setFundsforRndProjects(int fundsforRndProjects) {
		this.fundsforRndProjects = fundsforRndProjects;
	}
	public void setUniversityName(String universityName) {
		this.universityName = universityName;
	}
	public int getUniversityRanking() {
		return universityRanking;
	}
	public void setUniversityRanking(int universityRanking) {
		this.universityRanking = universityRanking;
	}
	public int getNumberOfCareerFair() {
		return numberOfCareerFair;
	}
	public void setNumberOfCareerFair(int numberOfCareerFair) {
		this.numberOfCareerFair = numberOfCareerFair;
	}
	public int getNumberOfProf() {
		return numberOfProf;
	}
	public void setNumberOfProf(int numberOfProf) {
		this.numberOfProf = numberOfProf;
	}
	public int getNumberOfDepartment() {
		return numberOfDepartment;
	}
	public void setNumberOfDepartment(int numberOfDepartment) {
		this.numberOfDepartment = numberOfDepartment;
	}
	public int getNumberOfCoursesOffered() {
		return numberOfCoursesOffered;
	}
	public void setNumberOfCoursesOffered(int numberOfCoursesOffered) {
		this.numberOfCoursesOffered = numberOfCoursesOffered;
	}
	
	
	
}
